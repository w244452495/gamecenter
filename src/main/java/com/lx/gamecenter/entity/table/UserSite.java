package com.lx.gamecenter.entity.table;

public class UserSite {
	
	private Integer	id;
	
	private Integer	userId;
	
	private String	nickname;
	
	private Integer	gameId;
	
	private Integer	tableId;
	
	private Integer	siteId;
	
	private String	siteHtmlId;
	
	private String	gameStatus;
	
	private String	userPoker;
	
	public Integer getId() {
		return id;
	}
	
	public void setId(Integer id) {
		this.id = id;
	}
	
	public Integer getUserId() {
		return userId;
	}
	
	public void setUserId(Integer userId) {
		this.userId = userId;
	}
	
	public String getNickname() {
		return nickname;
	}
	
	public void setNickname(String nickname) {
		this.nickname = nickname;
	}
	
	public Integer getGameId() {
		return gameId;
	}
	
	public void setGameId(Integer gameId) {
		this.gameId = gameId;
	}
	
	public Integer getTableId() {
		return tableId;
	}
	
	public void setTableId(Integer tableId) {
		this.tableId = tableId;
	}
	
	public Integer getSiteId() {
		return siteId;
	}
	
	public void setSiteId(Integer siteId) {
		this.siteId = siteId;
	}
	
	public String getSiteHtmlId() {
		return siteHtmlId;
	}
	
	public void setSiteHtmlId(String siteHtmlId) {
		this.siteHtmlId = siteHtmlId;
	}
	
	public String getGameStatus() {
		return gameStatus;
	}
	
	public void setGameStatus(String gameStatus) {
		this.gameStatus = gameStatus;
	}
	
	public String getUserPoker() {
		return userPoker;
	}
	
	public void setUserPoker(String userPoker) {
		this.userPoker = userPoker;
	}
	
}
